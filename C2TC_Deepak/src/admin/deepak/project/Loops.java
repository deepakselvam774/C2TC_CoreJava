package admin.deepak.project;

public class Loops {
	public static void main(String[] args) {
		// For loop
		for (int i=0;i<10;i++)
		{
			System.out.println("i= "+i); 
		}
		int j=0;
		while(j<10)
		{
			System.out.println("j= "+j);
			j++;
		}	
		int k=0;
		do
		{
			System.out.println("k= "+k);
			k++;
		
		}
		while(k<10);
		}

	}


